function [x0] = ferrari(a)
    a = a/a(1);

    a3 = [1, -a(3)/2, (a(2)*a(4)-4*a(5))/4, (4*a(3)*a(5)-a(5)*a(2)^2-a(4)^2)/8];
    k = cardano(a3);
    k0 = k(1);

    p = sqrt(2*k0 + a(2)^2/4 - a(3));
    q = (k0*a(2) - a(4))/(2*p);

    a21 = [1, a(2)/2 + p, k0 + q];
    a22 = [1, a(2)/2 - p, k0 - q];

    D1 = a21(2)^2 - 4*a21(1)*a21(3);
    D2 = a22(2)^2 - 4*a22(1)*a22(3);

    x0 = [(-a21(2)-sqrt(D1))/(2*a21(1)),(-a21(2)+sqrt(D1))/(2*a21(1)), (-a22(2)-sqrt(D2))/(2*a22(1)),(-a22(2)+sqrt(D2))/(2*a22(1))];
end

